package log;

/**
 * Интерфейс слушателя изменений лога.
 * Объекты, реализующие этот интерфейс, получают уведомления
 * при добавлении новых записей в источник лога.
 */
public interface LogChangeListener {
    /**
     * Вызывается при изменении содержимого лога.
     */
    void onLogChanged();
}
